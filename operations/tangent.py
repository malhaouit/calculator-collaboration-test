#!/bin/python3
"""A module for calculating the tangent value of a number"""
import math


def tangent(number):
    """Calculate the tangent of a given number.

    Parameters:
        number (int, float): The number in radians for which the tangent needs to be calculated.
    Returns:
    float: The tangent of the given number.
    """
   
    return math.tan(number)
